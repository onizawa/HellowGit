              <div class="question">
                UTMLの色調設定は？
              </div>
              <div class="answer">
                UTMLには「スタンダードシェード」、「エナメルシェード」の2タイプがあり、どちらも全層にわたり高い透光性を有しています。<br>
                「エナメルシェード」は、上層の彩度が抑えられており、切端部のステイニングにより透明感を際立たせることができる自由度の高いシェードです。<br>
                <br>
