                使用可能な焼成炉は？焼成炉の焼成最高温度が1550℃に達さない場合は使用できないのですか？
              </div>
              <div class="answer">
                所定の焼成スケジュールを使用していない場合は十分な透光性および発色が得られません。またその他のトラブルの原因になるため焼成条件を守ってご使用ください。
              </div>
