                  <li>フルジルコニア修復物、陶材築盛用フレームとも、前歯部 0.4mm、臼歯部 0.5mmの厚みは、ディスクの下半分に位置する場合の厚みです。</li>
                  <li>ラミネートベニアにおいて、陶材を築盛する場合には、ディスクの上半分に位置するフレームの厚みは0.8mm以上確保してください。</li>
