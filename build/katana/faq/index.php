                UTML、STMLはなぜ透光性が高いのですか？ガラスセラミックが配合されていますか？
              </div>
              <div class="answer">
                ガラスセラミックは配合されていません。ジルコニアディスクをジルコニア粉体から独自に開発することで高い透光性を付与しています。
