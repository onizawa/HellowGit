            <li class="faqList__item">
              <div class="question">
                焼成後の修復物の取扱い時の注意点は？
              </div>
              <div class="answer">
                STML、UTMLはLT、HT、HTMLよりも機械的特性に劣るため、適合調整、形態修正の際には過度の力を加えず、注水下又は補綴物を濡らした状態でダイヤモンドバーやダイヤモンド粒子を含むシリコンポイント等で作業をしてください。またブリッジ連結部やマメロン構造部等に鋭利な切込みを入れないでください。
              </div>
