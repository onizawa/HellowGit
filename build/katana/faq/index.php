                90分焼成後の取り出し時の注意事項は？
              </div>
              <div class="answer">
                修復物の破損を防ぐため、800℃で取り出し後、室温に戻るまで必ずセラミックファイバー性の焼成台（トレー）（例：ノリタケポーセレンマット）に置き徐冷してください。
              </div>
